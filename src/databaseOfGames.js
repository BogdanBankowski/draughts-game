export function transformIntoGameFormat(movesString) {
  const moves = movesString
    .split("\n")
    .map((e) => e.replace(/^\d+[.]?\s*/, ""))
    .join(" ")
    .split(" ")
    .filter((e) => e.match(/\d+[-x]\d+/));
  const output = [];

  for (let i = 0; i < moves.length; i++) {
    let splitMove = moves[i].split("-");
    if (splitMove.length === 1) {
      splitMove = moves[i].split("x");
      output.push({
        movFrom: parseInt(splitMove[0]),
        movTo: parseInt(splitMove[1]),
        isCapture: true,
      });
    } else {
      output.push({
        movFrom: parseInt(splitMove[0]),
        movTo: parseInt(splitMove[1]),
        isCapture: false,
      });
    }
  }
  return output;
}
